// SERIAL COMM. BAUDRATE FOR CONSOLE/USB(SERIAL)
#define CONSOLE_BAUD_RATE							(115200)			// 115200 [Baud] for Console/USB (Serial)
#define CONSOLE_COMM_CONFIG							(SERIAL_8N1)		// 8 sets data, Non parity bits, 1 stop bits
// SERIAL COMM. BAUDRATE FOR RS485(MODBUS RTU) BUS
#define RS485_BAUD_RATE								(19200)				// Max. Baud Rate of TM4 : 19200 [bps]
#define RS485_COMM_CONFIG1							(SERIAL_8N1)		// 8 sets data, Non parity bits, 1 stop bits
#define RS485_COMM_CONFIG2							(SERIAL_8N2)		// 8 sets data, Non parity bits, 2 stop bits
// SERIAL COMM. BAUDRATE FOR RS232(SERIAL) BUS
#define RS232_BAUD_RATE								(9600)				// Max. Baud Rate of Motor Controller : 9600 [bps]
#define RS232_COMM_CONFIG1							(SERIAL_8N1)		// 8 sets data, Non parity bits, 1 stop bits
#define RS232_COMM_CONFIG2							(SERIAL_8N2)		// 8 sets data, Non parity bits, 2 stop bits

// IIC BUS : Pin #21, #20
#define EXTERNAL_DEVICE_IIC_CONNECTED				(true)

// EXTERNAL INTERRUPT NUMBER OF ARDUINO MEGA
#define PIN_INTERRUPT_NUM_0							0					// External Interrupt pin : #2 (1)
#define PIN_INTERRUPT_NUM_1							1					// External Interrupt pin : #3 (2)
#if (!EXTERNAL_DEVICE_IIC_CONNECTED)
#define PIN_INTERRUPT_NUM_2							2 					// External Interrupt Pin : #21
#define PIN_INTERRUPT_NUM_3							3 					// External Interrupt Pin : #20
#endif
#define PIN_INTERRUPT_NUM_4							4 					// External Interrupt Pin : #19 (4)
#define PIN_INTERRUPT_NUM_5							5 					// External Interrupt Pin : #18 (3)


// ANALOG REFERENCE
#define ANALOG_READ_REF_TYPE						DEFAULT				// DEFAULT : INTERNAL, EXTERNAL (Must be Connected to External VCC)


////////////////////////////////////////////////////////////
// PIN ASSIGNMENTS (ARDUINO MEGA / CONTROLLINO MAXI)

#define _console									Serial 				// Serial #0 (RX0: #0,  TX0: #1 FOR ARDUNINO MEGA (USB OR PIN HEADER FOR CONTROLLINO MAXI))
#define _serial232									Serial2 			// Serial #2 (RX2: #17, TX2: #16 FOR ARDUINO MEGA (PIN HEADER FOR CONTROLLINO MAXI))
#define _serial485 									Serial3 			// Serial #3 (RX3: #15, TX3: #14 FOR ARDUINO MEGA (RS485 RX/TX FOR CONTROLLINO MAXI))
																		// TM4 : RS485(A)=(+), RS485(B)=(-) FOR CONTROLLINO MAXI(RS485 LINE DRIVER : SN65HVD08))

// BREW BUTTON SWITCH
#define BREW_BUTTON_SWITCH							2					// DIN(EINT) #2 FOR ARDUINO MEGA (CONTROLLINO_D0 FOR CONTROLLINO MAXI)

// ROTARY ENCODER WITH SWITCH
#define ROTARY_ENCODER_ST							3 					// DIN(EINT) #3 FOR ARDUINO MEGA (CONTROLLINO_D1 FOR CONTROLLINO MAXI)
#define ROTARY_ENCODER_DI							4					// DIN       #4 FOR ARDUINO MEGA (CONTROLLINO_D2 FOR CONTROLLINO MAXI)
#define ROTARY_ENCODER_SW							5					// DIN(POLL) #5 FOR ARDUINO MEGA (CONTROLLINO_D3 FOR CONTROLLINO MAXI)

// BREW BUTTON LED (NOT ATTACHED)
#define BREW_BUTTON_LED								6					// DOUT #6 FOR ARDUINO MEGA (CONTROLLINO_D4 FOR CONTROLLINO MAXI)

// LED PIXEL FOR VISUALIZATION
#define NEOPIXEL_DIN								7					// DOUT #7 FOR ARDUINO MEGA (CONTROLLINO_D5 FOR CONTROLLINO MAXI)

// PROPORTIONAL CONTROLLED SOLENOID VALVE
