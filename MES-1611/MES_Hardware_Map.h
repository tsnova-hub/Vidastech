// I2C BUS (TEXT LCD MODULE ON I2C BUS)
#define I2C_SDA										20					// DIO  #20 FOR ARDUINO MEGA (PIN HEADER FOR CONTROLLINO MAXI)
#define I2C_SCL										21					// DOUT #21 FOR ARDUINO MEGA (PIN HEADER FOR CONTROLLINO MAXI)

// SOLENOID VALVE
#define BREW_SOLENOID_VALVE							28					// DOUT(RLY) #28 FOR ARDUINO MEGA (CONTROLLINO_R6 FOR CONTROLLINO MAXI)
// #define AUX_SOLENOID_VALVE						29					// DOUT(RLY) #29 FOR ARDUINO MEGA (CONTROLLINO_R7 FOR CONTROLLINO MAXI)

// OPTICAL FIBER AMP POWER (AUTO-FLUSHING SENSOR)
#define OPTICAL_FIBER_AMP_POWER						29					// DOUT(RLY) #29 FOR ARDUINO MEGA (CONTROLLINO_R7 FOR CONTROLLINO MAXI)
#define OPTICAL_FIBER_SIGNAL						A6					// DIN       A6 FOR ARDUINO MEGA (CONTROLLINO_A6 FOR CONTROLLINO MAXI)
#define OPTICAL_FIBER_DIAGNOSIS						A7					// DIN       A7 FOR ARDUINO MEGA (CONTROLLINO_A7 FOR CONTROLLINO MAXI)

// TEXT LCD MODULE (BACKLIGHT MODE - NOT ATTACHED)
#define LCD_BACKLIGHT_LSB							42					// DOUT #42 FOR ARDUINO MEGA (PIN HEADER FOR CONTROLLINO MAXI)
#define LCD_BACKLIGHT_MSB							43					// DOUT #43 FOR ARDUINO MEGA (PIN HEADER FOR CONTROLLINO MAXI)

// SPI BUS (RTC AND ETHERNET CHIPSET ON SPI BUS)
// INTERFACE FOR AUX. DEVICE WITH SPI
