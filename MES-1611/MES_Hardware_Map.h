// PROPORTIONAL CONTROLLED SOLENOID VALVE (NOT ATTACHED)
#define PCTL_SOLENOID_VALVE							11					// DOUT(PWM) #11 FOR ARDUINO MEGA (CONTROLLINO_D9 FOR CONTROLLINO MAXI)

// AUX. GPIO INTERFACE (EINT, CH1)
#define AUX_GPIO_EINT								19					// DIN(EINT) #19 FOR ARDUINO MEGA (CONTROLLINO_IN1 FOR CONTROLLINO MAXI)
#define AUX_GPIO_CH1								13					// DOUT(PWM) #13 FOR ARDUINO MEGA (CONTROLLINO_D11 FOR CONTROLLINO MAXI)

// BLDC MOTOR CONTROLLER FOR PUMP
#define BLDC_MOTOR_SPEED							9					// DOUT(PWM) #9 FOR ARDUINO MEGA (CONTROLLINO_D7 FOR CONTROLLINO MAXI)
#define BLDC_MOTOR_STOP								22					// DOUT(RLY) #22 FOR ARDUINO MEGA (CONTROLLINO_R0 FOR CONTROLLINO MAXI)
